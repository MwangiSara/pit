# Append/Remove Items in a List
# This is a List we use []
items = ['Kisumu','Eldoret', 'Nakuru']
print(items)

# print type of items
print(type(items))

# Append a Town
items.append('Mombasa')

# Print items again, observe Mombasa is in the List
print(items)

# Remove a town, 
items.remove('Eldoret')

# observe Eldoret is removed from the List
print(items)

